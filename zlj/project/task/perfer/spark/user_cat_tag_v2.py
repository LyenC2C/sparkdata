def sort_limit(y):
    lv=[]
    for i in y:
        lv.append(i.split('_'))
    ts=[(i[0],float(i[1])) for index, i in enumerate(sorted(lv, key=lambda t: float(t[-1]), reverse=True)) if index < 11]
    v=sum([v for k,v in ts])
    return '\t'.join([i[0]+'_'+str(round(i[1]/v,4)) for i in ts])

