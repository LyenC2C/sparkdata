CREATE TABLE t_zlj_base_ec_item_dev_itemin
  AS
    SELECT t1.*
    FROM
      (
        SELECT
          item_id,

          cat_id,
          root_cat_id,
          root_cat_name,
          brand_id,
          brand_name,
          bc_type,
          price,
          shop_id,
          location
        FROM t_base_ec_item_dev
        WHERE ds = 20162016
              AND item_id RLIKE '^\\d+$'
      ) t1

      JOIN
      (
        SELECT
          DISTINCT item_id
        FROM t_base_ec_item_feed_dev
        WHERE ds > 20150101 AND user_id IS NOT NULL AND item_id IS NOT NULL AND item_id RLIKE '^\\d+$' AND
              user_id RLIKE '^\\d+$'
      ) t2 ON t1.item_id = t2.item_id;


